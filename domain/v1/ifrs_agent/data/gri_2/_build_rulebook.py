def remap_dp_in_str(s: str) -> str:
    for old, new in DP_REPLACEMENTS:
        s = s.replace(old, new)
    return s


def remap_rb_id(rid: str) -> str:
    for old, new in RB_ID_REPLACEMENTS:
        if rid == old:
            return new
    return rid


def remap_obj(o):
    if isinstance(o, dict):
        out = {}
        for k, v in o.items():
            if k == "rulebook_id" and isinstance(v, str):
                out[k] = remap_rb_id(v)
            else:
                out[k] = remap_obj(v)
        return out
    if isinstance(o, list):
        return [remap_obj(v) for v in o]
    if isinstance(o, str):
        return remap_dp_in_str(o)
    return o


