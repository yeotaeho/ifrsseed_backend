def remap_str(s: str) -> str:
    for old, new in DP_REPLACEMENTS:
        s = s.replace(old, new)
    return s


def remap_obj(o):
    if isinstance(o, dict):
        return {k: remap_obj(v) for k, v in o.items()}
    if isinstance(o, list):
        return [remap_obj(v) for v in o]
    if isinstance(o, str):
        return remap_str(o)
    return o


